class FuelOilResult {
  final double ashWork;
  final double vanadiumWork;
  final double carbonWork;
  final double hydrogenWork;
  final double sulfurWork;
  final double oxygenWork;
  final double burnWorkTemp;

  const FuelOilResult({
    required this.ashWork,
    required this.vanadiumWork,
    required this.carbonWork,
    required this.hydrogenWork,
    required this.sulfurWork,
    required this.oxygenWork,
    required this.burnWorkTemp,
  });
}